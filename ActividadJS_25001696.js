var mes;
var dia;

mes = prompt ("Ingresa tu mes en el que naciste");
dia = prompt ("Ingresa tu dia de nacimiento");
if (parseInt (mes,10) == 3 && parseInt (dia,10) >= 21) {alert("Tu signo es Aries")}
else if (parseInt (mes,10) == 4 && parseInt (dia,10) <= 20) {alert("Tu signo es Aries")}

else if (parseInt (mes,10) == 4 && parseInt (dia,10) >= 21) {alert("Tu signo es Tauro")}
else if (parseInt (mes,10) == 5 && parseInt (dia,10) <= 20) {alert("Tu signo es Tauro")}

else if (parseInt (mes,10) == 5 && parseInt (dia,10) >= 21) {alert("Tu signo es Geminis")}
else if (parseInt (mes,10) == 6 && parseInt (dia,10) <= 20) {alert("Tu signo es Geminis")}

else if (parseInt (mes,10) == 6 && parseInt (dia,10) >= 21) {alert("Tu signo es Cancer")}
else if (parseInt (mes,10) == 7 && parseInt (dia,10) <= 22) {alert("Tu signo es Cancer")}

else if (parseInt (mes,10) == 7 && parseInt (dia,10) >= 23) {alert("Tu signo es Leo")}
else if (parseInt (mes,10) == 8 && parseInt (dia,10) <= 23) {alert("Tu signo es Leo")}

else if (parseInt (mes,10) == 8 && parseInt (dia,10) >= 24) {alert("Tu signo es Virgo")}
else if (parseInt (mes,10) == 9 && parseInt (dia,10) <= 22) {alert("Tu signo es Virgo")}

else if (parseInt (mes,10) == 9 && parseInt (dia,10) >= 23) {alert("Tu signo es Libra")}
else if (parseInt (mes,10) == 10 && parseInt (dia,10) <= 23) {alert("Tu signo es Libra")}

else if (parseInt (mes,10) == 10 && parseInt (dia,10) >= 24) {alert("Tu signo es Escorpio")}
else if (parseInt (mes,10) == 11 && parseInt (dia,10) <= 22) {alert("Tu signo es Escorpio")}

else if (parseInt (mes,10) == 11 && parseInt (dia,10) >= 23) {alert("Tu signo es Sagitario")}
else if (parseInt (mes,10) == 12 && parseInt (dia,10) <= 21) {alert("Tu signo es Sagitario")}

else if (parseInt (mes,10) == 12 && parseInt (dia,10) >= 22) {alert("Tu signo es Capricornio")}
else if (parseInt (mes,10) == 1 && parseInt (dia,10) <= 22) {alert("Tu signo es Capricornio")}

else if (parseInt (mes,10) == 1 && parseInt (dia,10) >= 21) {alert("Tu signo es Acuario")}
else if (parseInt (mes,10) == 2 && parseInt (dia,10) <= 19) {alert("Tu signo es Acuario")}

else if (parseInt (mes,10) == 2 && parseInt (dia,10) >= 20) {alert("Tu signo es Piscis")}
else if (parseInt (mes,10) == 3 && parseInt (dia,10) <= 20) {alert("Tu signo es Piscis")}